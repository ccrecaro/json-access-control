export function numberAdd(arg1: number, arg2: number) {
    return arg1 + arg2;
}

export function numberSubtract(arg1: number, arg2: number) {
    return arg1 - arg2;
}

export function numberMultiply(arg1: number, arg2: number) {
    return arg1 * arg2;
}

export function numberDivide(arg1: number, arg2: number) {
    return arg1 / arg2;
}

export function integerMod(arg1: number, arg2: number) {
    return arg1 % arg2;
}

export function abs(arg1: number) {
    return Math.abs(arg1);
}

export function numberAbs(arg1: number) {
    return Math.abs(arg1);
}

export function round(arg1: number) {
    return Math.round(arg1);
}

export function floor(arg1: number) {
    return Math.floor(arg1);
}